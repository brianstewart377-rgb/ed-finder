def port_cp_cost(tier: int, paid_port_index: int) -> tuple[int, int]:
    """
    Return the escalating CP cost for a paid port placement.

    paid_port_index is zero-based within the port tier. Primary-port
    exemptions should be handled by the caller by skipping this cost.
    """
    if tier == 3:
        return _t3_yellow_cost(paid_port_index), _t3_green_cost(paid_port_index)
    if tier == 2:
        return _t2_yellow_cost(paid_port_index), _t2_green_cost(paid_port_index)
    return 0, 0

def _t2_yellow_cost(n: int) -> int:
    """Yellow CP cost of the (n+1)th T2 port (0-indexed)."""
    if n < len(_T2_PORT_COSTS_YELLOW):
        return _T2_PORT_COSTS_YELLOW[n]
    # Beyond table: extrapolate linearly
    return _T2_PORT_COSTS_YELLOW[-1] + (n - len(_T2_PORT_COSTS_YELLOW) + 1) * T2_YELLOW_EXTRAPOLATION_STEP

def _t2_green_cost(n: int) -> int:
    if n < len(_T2_PORT_COSTS_GREEN):
        return _T2_PORT_COSTS_GREEN[n]
    return _T2_PORT_COSTS_GREEN[-1] + (n - len(_T2_PORT_COSTS_GREEN) + 1) * T2_GREEN_EXTRAPOLATION_STEP

def _t3_yellow_cost(n: int) -> int:
    if n < len(_T3_PORT_COSTS_YELLOW):
        return _T3_PORT_COSTS_YELLOW[n]
    return _T3_PORT_COSTS_YELLOW[-1] + (n - len(_T3_PORT_COSTS_YELLOW) + 1) * T3_YELLOW_EXTRAPOLATION_STEP

def _t3_green_cost(n: int) -> int:
    if n < len(_T3_PORT_COSTS_GREEN):
        return _T3_PORT_COSTS_GREEN[n]
    return _T3_PORT_COSTS_GREEN[-1] + (n - len(_T3_PORT_COSTS_GREEN) + 1) * T3_GREEN_EXTRAPOLATION_STEP


class _DummyFacility:
    """Minimal facility-like object for cost simulation."""
    def __init__(self, tier: int):
        self.tier       = tier
        self.id         = f'_sim_t{tier}'
        self.is_port    = True
        self.yellow_cp_cost = 0
        self.green_cp_cost  = 0

def _dummy_t2_port() -> '_DummyFacility':
    return _DummyFacility(2)

def _dummy_t3_port() -> '_DummyFacility':
    return _DummyFacility(3)
